    месяца.</p>
<p>6. Все платежи осуществляются Членом кооператива посредством внесения денежных средств на расчётный счёт Кооператива.
    Все затраты, связанные с перечислением денежных средств на расчётный счёт Кооператива, в том числе по оплате
    комиссии банка за денежный перевод (услуги банка), Член кооператива несёт самостоятельно.</p>
<p>7. По соглашению сторон, денежные средства, поступающие от Члена кооператива, вне зависимости от назначения платежа,
    указанного в платёжном документе, засчитываются Кооперативом в счёт оплаты:</p>
<p>- в первую очередь, вступительного взноса;</p>
<p>- во вторую очередь, паевого взноса.</p>
<p>8. При наступлении у Члена кооператива права на приобретение автомобиля в соответствии с условиями Договора,
