<p>1) требовать от Члена кооператива своевременного и неукоснительного соблюдения и выполнения, принятых на себя
    обязательств в соответствии с Договором, действующими законодательными актами, Уставом Кооператива, внутренними
    положениями Кооператива, решениями органов управления Кооператива;</p>
<p>2) исключить Члена кооператива из членов Кооператива решением общего собрания в случае неисполнения им без
