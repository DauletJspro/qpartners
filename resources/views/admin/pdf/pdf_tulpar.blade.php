    третьему лицу (при условии вступления последнего в Члены кооператива);</p>
<p>2) по своему усмотрению и в любое время выйти из Кооператива и получить сумму своего паевого взноса, в порядке,
    определяемом Уставом Кооператива, решением общего собрания членов Кооператива;</p>
<p>3) участвовать в деятельности Кооператива, вносить предложения, присутствовать на собраниях Кооператива, принимать
    участие в голосовании;</p>
<p>4) получать от органов управления и иных органов Кооператива информацию об их деятельности;</p>
<p>5) осуществлять иные права, предусмотренные Уставом и действующим законодательством РК.</p>
<h4 align="center">Раздел 4. Порядок предоставления беспроцентного целевого займа на приобретение недвижимости</h4>
<p>1. Минимальный размер паевого взноса, после внесения, которого у Члена кооператива возникает право на получения
    беспроцентного целевого займа от Кооператива на приобретение Членом кооператива автомобиля, составляет не менее чем
    30/50 (________________) процентов от Ориентировочной стоимости автомобиля, указанной в пункте 4 раздела 1 Договора.
</p>
<p>2. При внесении Членом кооператива вступительного взноса и минимального размера паевого взноса он включается в
    программу на получение беспроцентного целевого займа от Кооператива на приобретение автомобиля.</p>
<p>Под программой на получение беспроцентного целевого займа от Кооператива (далее по тексту – Программа) понимается
    предоставление Кооперативом беспроцентных целевых займов членам Кооператива на приобретение ими в свою собственность
    движимого имущества за счёт накоплений, составленных из паевых взносов всех членов Кооператива.</p>
<p>Программа предусматривает очерёдность предоставления беспроцентных целевых займов членам Кооператива, определяющими
    факторами которого являются полнота внесённого паевого взноса и время произведённого платежа (дата, час, минута,
    секунда).</p>
<p>3. Беспроцентный целевой займ на приобретение автомобиля под его залог и с рассрочкой платежа предоставляется Члену
    кооператива в течение 10 (десяти) календарных дней с момента истечения 6 (шести) месяцев, исчисляемой с момента
    внесения вступительного взноса и минимального размера паевого взноса. При этом кооператив имеет право инициировать
    предоставление беспроцентного целевого займа на приобретение автомобиля под его залог и с рассрочкой платежа, с
    согласия Члена кооператива досрочно.</p>
<p>4. Член кооператива заключает с Кооперативом договор беспроцентного целевого займа с рассрочкой платежа на следующих
    условиях:</p>
<p>1) Член кооператива обязан явиться для заключения договора беспроцентного целевого займа на приобретение автомобиля
    под его залог ко времени и к месту, указанному Кооперативом;</p>
<p>2) в течение 7 (семи) календарных дней Член кооператива обязан с участием представителя Кооператива заключить с
    продавцом автомобиля договор купли-продажи. С этой целью Член кооператива обязан направить Кооперативу письменное
    уведомление о намерении заключить названный договор через нотариальное удостоверение с таким расчётом, чтобы
    представитель Кооператива имел достаточный срок для своевременного прибытия к месту заключения договора. В
    уведомлении должны быть указаны местонахождение нотариальной конторы, фамилия, имя и отчество нотариуса и время
    совершения нотариальных действий;</p>
<p>3) представитель Кооператива, получив уведомление, указанное в подпункте 2 п.4 раздела 4 Договора, обязуется
    своевременно прибыть к нотариусу для заключения договора купли-продажи. Член кооператива обязуется зарегистрировать
    возникновение права собственности на автомобиль в органе дорожной полиции;</p>
<p>4) Член кооператива обязан заключить с Кооперативом договор залога автомобиля в обеспечение своих обязательств по
    договору беспроцентного целевого займа на приобретение автомобиля под его залог в течение 7 (семи) календарных дней
    с даты получения уведомления о регистрации договора купли-продажи. С этой целью Член кооператива обязан направить
    Кооперативу письменное уведомление о намерении заключить названный договор с таким расчётом, чтобы представитель
    Кооператива имел достаточный срок для своевременного прибытия к месту заключения договора. В уведомлении должны быть
    указаны место и время заключения договора;</p>
<p>5) представитель Кооператива, получив уведомление, указанное в подпункте 4 п.4 раздела 4 Договора, обязуется
    своевременно прибыть для заключения договора залога. Член кооператива обязуется зарегистрировать договор залога
    автомобиля, заключённый с Кооперативом, в органе дорожной полиции с участием представителя Кооператива;</p>
<p>6) беспроцентный целевой займ предоставляется Кооперативом Члену кооператива путём зачисления денег на счёт продавца
