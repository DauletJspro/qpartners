    то очередь на получение беспроцентного займа передаётся следующему очереднику, а Член кооператива передвигается в
    очередь на позицию ниже, то есть вместо предыдущего очередника к которому перешла очередь Члена кооператива;</p>
<p>8) своевременно являться по уведомлениям Кооператива для решения организационных вопросов по исполнению Договора, за
    исключением случаев невозможности явки по уважительной причине;</p>
<p>9) соблюдать положения действующего законодательства РК, Устава Кооператива, внутренних положений Кооператива,
    решений органов управления Кооператива и условия Договора;</p>
<p>10) выполнять свои обязательства перед Кооперативом по участию в его хозяйственной деятельности.</p>
