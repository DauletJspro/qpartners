<p>2) своевременно вносить установленные паевые и иные взносы в соответствии с Уставом и положениями Кооператива,
    условиями Договора и решениями, принятыми органами управления Кооператива;</p>
<p>3) не препятствовать осуществлению предусмотренных уставом Кооператива прав и исполнению обязанностей других членов
