    подбор автомобиля;</p>
<p>2) проводить мероприятия, необходимые для приобретения Членом кооператива автомобиля посредством его членства в
    Кооперативе;</p>
<p>3) размещать список очередников на право получения заёмных денежных средств на приобретение автомобиля на своём
    сайте __________, на который Член кооператива может иметь право доступа после включения его в программу на получение
    беспроцентного целевого займа от Кооператива на приобретение автомобиля, и соответственно, после выдачи ему логина
    и пароля на свой личный кабинет.</p>
<p>2. Член кооператива обязуется:</p>
<p>1) соблюдать Устав Кооператива и условия Договора, выполнять решения, принятые органами управления Кооператива, и
