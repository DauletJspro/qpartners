<h4 align="center">Раздел 1. Предмет договора </h4>
<p>1. Настоящий Договор является гражданско-правовым договором между Кооперативом и Членом кооператива об установлении и
    регулировании взаимных имущественных прав, обязанностей и ответственности.</p>
<p>2. В целях удовлетворения потребностей Члена кооператива в автомобиле, Член кооператива вступает в Кооператив.
    Подписав Договор, Член кооператива выражает согласие и присоединяется к единым условиям членства в Кооперативе,
    изложенные в Уставе Кооператива, с которым он ознакомлен заранее.</p>
<p>3. Настоящий Договор определяет механизм и порядок предоставления Кооперативом Члену кооператива беспроцентного
    целевого займа на приобретение автомобиля под его залог на условиях рассрочки платежа при условии оплаты Членом
    кооператива вступительного, паевого, членского взносов в размере, порядке и сроки, установленные Уставом,
    внутренними положениями Кооператива и Договором.</p>
<p>4. Ориентировочная стоимость приобретаемого автомобиля составляет ______ (_________) тенге, которая определена лично
    Членом кооператива на момент вступления в Кооператив на основе ориентировочных параметров, сравнительных данных о
    рыночной стоимости аналогичных транспортных средств.</p>
<p>Ориентировочная стоимость приобретаемого автомобиля может быть изменена по инициативе Члена кооператива в сторону
    увеличения в течение 14 календарных дней с момента подписания сторонами Договора. В таком случае такие изменения
    должны быть внесены в Договор путём заключения дополнительного соглашения с пропорциональным увеличением взносов,
    указанных в пунктах 2 и 3 раздела 2 Договора.</p>
<p>При этом, цена приобретаемого автомобиля должна составлять не менее 3 000 000 (трех миллионов) тенге и не свыше
    9 000 000 (девяти миллионов) тенге.</p>
<p>5. Ориентировочная стоимость приобретаемого автомобиля остаётся действующей до момента приобретения в собственность
    Члена кооператива конкретного автомобиля, поиск и выбор которого Член кооператива производит самостоятельно. При
    этом автомобиль должен быть не ниже 2000 года выпуска.</p>
<p>6. Член кооператива несёт риск удорожания автомобиля до момента его приобретения в собственность.</p>

